  CREATE OR REPLACE EDITIONABLE PROCEDURE "C##TEST"."DEMAND_COUNT" (start_time IN TIMESTAMP, end_time IN TIMESTAMP, goods_id IN NUMBER, count_ out NUMBER)
IS
BEGIN
    select SUM(GOOD_COUNT) into count_ from SALES where GOOD_ID = goods_id and CREATE_DATE between start_time and end_time;
    if count_= null
    then
        count_:=0;
    end if;
END demand_count;

/
